function toHiragana(str) {
    return str.replace(/[\u30a1-\u30f6]/g, function(match) {
        var chr = match.charCodeAt(0) - 0x60;
        return String.fromCharCode(chr);
    });
}

let POKE_DB = [];

function parseCSV() {
    const lines = CSV_TEXT.trim().split('\n');
    for (let i = 1; i < lines.length; i++) {
        const row = lines[i].replace('\r', '').split(',');
        if (row.length < 10) continue;

        const id = row[0];
        const name = row[1];
        const form = row[2];
        const displayName = (form === '通常' || !form) ? name : `${name} (${form})`;
        const searchKey = toHiragana(displayName);

        POKE_DB.push({
            id: id,
            name: name,
            form: form,
            displayName: displayName,
            searchKey: searchKey,
            types: [row[3], row[4]].filter(t => t),
            stats: {
                h: parseInt(row[5]), a: parseInt(row[6]), b: parseInt(row[7]),
                c: parseInt(row[8]), d: parseInt(row[9]), s: parseInt(row[10])
            }
        });
    }
}

function handleSearch(pid) {
    const input = document.getElementById(`${pid}-search`);
    const suggestionsBox = document.getElementById(`${pid}-suggestions`);
    const rawQuery = input.value.trim();

    if (rawQuery.length < 1) {
        suggestionsBox.style.display = 'none';
        return;
    }

    const query = toHiragana(rawQuery);
    const matched = POKE_DB.filter(p => p.searchKey.startsWith(query)).slice(0, 8); 

    suggestionsBox.innerHTML = '';
    if (matched.length > 0) {
        suggestionsBox.style.display = 'block';
        matched.forEach(p => {
            const div = document.createElement('div');
            div.className = 'suggestion-item';
            div.innerText = p.displayName;
            div.onclick = () => selectPokemon(pid, p);
            suggestionsBox.appendChild(div);
        });
    } else {
        suggestionsBox.style.display = 'none';
    }
}

