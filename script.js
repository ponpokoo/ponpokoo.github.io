function selectPokemon(pid, data) {
    currentPokemon[pid] = data;
    document.getElementById(`${pid}-search`).value = '';
    document.getElementById(`${pid}-suggestions`).style.display = 'none';
    document.getElementById(`${pid}-info`).style.display = 'flex';
    document.getElementById(`${pid}-name`).innerText = data.displayName;
    document.getElementById(`${pid}-img`).src = `https://raw.githubusercontent.com/PokeAPI/sprites/master/sprites/pokemon/${data.id}.png`;
    const typeContainer = document.getElementById(`${pid}-types`);
    typeContainer.innerHTML = '';
    data.types.forEach(t => {
        const badge = document.createElement('span');
        badge.className = 'type-badge';
        badge.innerText = t;
        badge.style.backgroundColor = TYPE_COLORS[t] || '#999';
        typeContainer.appendChild(badge);
    });
    STATS.forEach(s => document.getElementById(`${pid}-base-${s}`).value = data.stats[s]);
    updateMegaButtonState(pid);
    calc(pid);
}

function toggleMega(pid) {
    const current = currentPokemon[pid];
    if (!current) {
        alert("ポケモンが選択されていません。");
        return;
    }
    const variants = POKE_DB.filter(p => p.id === current.id);
    const megaVariants = variants.filter(p => 
        p.form === '通常' || p.form.includes('メガ') || p.form.includes('ゲンシ')
    );
    if (megaVariants.length <= 1) return;
    const currentIndex = megaVariants.findIndex(p => p.displayName === current.displayName);
    const nextIndex = (currentIndex + 1) % megaVariants.length;
    selectPokemon(pid, megaVariants[nextIndex]);
}

function updateMegaButtonState(pid) {
    const current = currentPokemon[pid];
    const btn = document.getElementById(`${pid}-mega-btn`);
    if (!current || !btn) return;
    if (current.displayName.includes('メガ') || current.displayName.includes('ゲンシ')) {
        btn.classList.add('active');
    } else {
        btn.classList.remove('active');
    }
}

function toggleNature(id, stat, type) {
    const current = state[id];
    if (type === 'up') {
        current.natureUp = (current.natureUp === stat) ? null : stat;
        if (current.natureDown === stat) current.natureDown = null;
    } else {
        current.natureDown = (current.natureDown === stat) ? null : stat;
        if (current.natureUp === stat) current.natureUp = null;
    }
    updateNatureUI(id);
    calc(id);
}

function updateNatureUI(id) {
    ['a','b','c','d','s'].forEach(s => {
        const p = document.getElementById(`${id}-btn-plus-${s}`);
        const m = document.getElementById(`${id}-btn-minus-${s}`);
        p.className = 'btn-nature'; m.className = 'btn-nature';
        if (state[id].natureUp === s) p.classList.add('active-plus');
        if (state[id].natureDown === s) m.classList.add('active-minus');
    });
}

function setEv(id, stat, val) {
    if(val === 252) {
        let sum = 0;
        STATS.forEach(s => { if(s !== stat) sum += (parseInt(document.getElementById(`${id}-ev-${s}`).value) || 0); });
        val = Math.floor(Math.min(252, 510 - sum) / 4) * 4;
    }
    document.getElementById(`${id}-ev-${stat}`).value = val;
    calc(id);
}

function resetEvs(id) {
    STATS.forEach(s => document.getElementById(`${id}-ev-${s}`).value = 0);
    calc(id);
}

function updateEvFromRes(id, stat) {
    const inputVal = parseInt(document.getElementById(`${id}-res-${stat}`).value);
    if (isNaN(inputVal)) { calc(id); return; }
    const foundEv = findMinEvForTarget(id, stat, inputVal);
    if (foundEv !== -1) document.getElementById(`${id}-ev-${stat}`).value = foundEv;
    calc(id);
}

function adjustRealValueByDelta(id, stat, delta) {
    const currentVal = parseInt(document.getElementById(`${id}-res-${stat}`).value) || 0;
    const targetVal = currentVal + delta;
    const foundEv = findMinEvForTarget(id, stat, targetVal);
    if (foundEv !== -1) {
        document.getElementById(`${id}-ev-${stat}`).value = foundEv;
        calc(id);
    }
}

function findMinEvForTarget(id, stat, targetVal) {
    let otherEvSum = 0;
    STATS.forEach(s => {
        if(s !== stat) otherEvSum += (parseInt(document.getElementById(`${id}-ev-${s}`).value) || 0);
    });
    const maxAllowedEv = 510 - otherEvSum;
    for (let ev = 0; ev <= 252; ev++) {
        if (ev > maxAllowedEv) break;
        const val = calcSingleStat(id, stat, ev);
        if (val === targetVal) return ev;
    }
    return -1;
}

function calcSingleStat(id, s, evOverride) {
    const level = parseInt(document.getElementById(`${id}-level`).value) || 50;
    const base = parseInt(document.getElementById(`${id}-base-${s}`).value) || 0;
    const iv = parseInt(document.getElementById(`${id}-iv-${s}`).value) || 0;
    const ev = (evOverride !== undefined) ? evOverride : (parseInt(document.getElementById(`${id}-ev-${s}`).value) || 0);
    
    if (s === 'h') {
        if (base === 1) return 1;
        return Math.floor(((base * 2 + iv + Math.floor(ev / 4)) * level) / 100) + level + 10;
    } else {
        let natureMult = 1.0;
        if (state[id].natureUp === s) natureMult = 1.1;
        if (state[id].natureDown === s) natureMult = 0.9;
        return Math.floor((Math.floor(((base * 2 + iv + Math.floor(ev / 4)) * level) / 100) + 5) * natureMult);
    }
}

function calc(id) {
    let totalEv = 0;
    STATS.forEach(s => {
        const ev = parseInt(document.getElementById(`${id}-ev-${s}`).value) || 0;
        totalEv += ev;
        const val = calcSingleStat(id, s);
        document.getElementById(`${id}-res-${s}`).value = val; 
    });
    const totalEl = document.getElementById(`${id}-total`);
    totalEl.innerText = totalEv;
    totalEl.className = totalEv > 510 ? 'text-alert' : '';
}
